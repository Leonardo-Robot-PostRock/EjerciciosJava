package clasesYobjetos.Agregacion;

public class Modem {
    private String marca;

    public Modem() {
    }

    public Modem(String marca) {
        this.marca = marca;
    }

    public String getMarca() {
        return this.marca;
    }

    public void setMarca(String val) {
        this.marca = val;
    }
}
