public class ejerciciosCiclos {
    public static void main(String[] args) {
        for(int i=0; i<=10; i++){
            System.out.println("Valor de i " + i);
        }
        System.out.println("-------------------");
        for(int i=10; i>=0; i--){
            System.out.println("Valor de i " + i);
        }
    }
}
